static const char* LTMenu_main = "LeafTools NEO";
static const char* LTHelp_main = "Hello, world!\nFill me in later with something more descriptive.";

static const char* LTMenu_test = "Test cheat";
static const char* LTHelp_test = "Test cheat description goes here.";

///////////////////////////////////////////////////////////////////////////////
// Menu Setup
///////////////////////////////////////////////////////////////////////////////

void BuildLeafToolsMenu()
{
	int index;

	index = new_spoiler(LTMenu_main);
	set_note(LTHelp_main, index);
	{
		index = new_entry(LTMenu_test, &TestCheat);
		set_note(LTHelp_test, index);
	}
	exit_spoiler();
}

///////////////////////////////////////////////////////////////////////////////
// Cheats
///////////////////////////////////////////////////////////////////////////////

void TestCheat()
{
	WRITEU16(0x31F2DBF0, 0x00002117);
}
